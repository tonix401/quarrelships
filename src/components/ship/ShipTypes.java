package components.ship;

/**
 * Enumeration of different ship types available in the QuarrelShips game.
 * Each ship type has a different size and configuration:
 * 
 * - DESTROYER: 2 cells
 * - SUBMARINE: 3 cells
 * - CRUISER: 3 cells
 * - BATTLESHIP: 4 cells
 * - CARRIER: 5 cells
 * 
 * @author QuarrelShips Development Team
 * @version 1.0
 */
public enum ShipTypes {
    /** Destroyer ship - 2 cells long */
    DESTROYER,
    
    /** Submarine ship - 3 cells long */
    SUBMARINE,
    
    /** Cruiser ship - 3 cells long */
    CRUISER,
    
    /** Battleship - 4 cells long */
    BATTLESHIP,
    
    /** Carrier ship - 5 cells long */
    CARRIER
}
